#include "wire.h"
#include <string>

using namespace std;

wire::wire()
{
	from = "";
	to = "";
	wireName = "";
	value = 0;
}

wire::wire(string source, string destination, string name)
{
	from = source;
	to = destination;
	wireName = name;
	value = 0;
}

wire::wire(string source, string destination, string name, int val)
{
	from = source;
	to = destination;
	wireName = name;
	value = val;
}

string wire::get_from()
{
	return from;
}

string wire::get_to()
{
	return to;
}

string wire::get_name()
{
	return wireName;
}

int wire::get_value()
{
	return value;
}

void wire::set_from(string source)
{
	from = source;
}

void wire::set_to(string destination)
{
	to = destination;
}

void wire::set_name(string name)
{
	wireName = name;
}

void wire::set_value(int val)
{
	value = val;
}
