#ifndef GINPUT_H_
#define GINPUT_H_


#include<iostream>

using namespace std;

class ginput
{
  public:

  ginput()
  {
   this->val = false; 
   this->name = name;
  }

  ginput(bool val)
  {
    this->val = val;
  }

  bool value()
  {
    return this->val;
  }  
  void set_value(bool value)
  {
    this->val = value;
  }
  void set_name (char name)
  {
    this->name = name;
  }
  char get_name()
  {
    return this->name;
  }
  private:
  bool val;
  char name;

};

#endif /*GINPUT_H_*/
